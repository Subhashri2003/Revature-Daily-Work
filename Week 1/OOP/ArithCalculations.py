class ArithCalculations:
    def __init__(self,num1,num):
        self.num1=num1
        self.num2=num

    def add(self):
        return self.num1+self.num2
    def sub(self):
        return self.num1-self.num2
    def mul(self):
        return self.num1*self.num2
    def div(self):
        return self.num1/self.num2
    def idiv(self):
        return self.num1//self.num2